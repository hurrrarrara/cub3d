#include <unistd.h>
#include "basics.h"

static size_t	_strlen(const char *str)
{
	size_t	i;

	i = 0;
	if (str)
		while (str[i])
			i++;
	return (i);
}

void	ft_putstr_fd(char *s, int fd)
{
	if (fd >= 0)
		write(fd, s, _strlen(s));
}
